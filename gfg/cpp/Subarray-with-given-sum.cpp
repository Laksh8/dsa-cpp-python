class Solution
{
    public:
    //Function to find a continuous sub-array which adds up to a given number.
    vector<int> subarraySum(vector<int>arr, int n, long long s)
    {
        // Your code here
        long long sum = arr[0];
        int i=0,j=0 ;
        while(j<n){
            sum = sum + arr[j];
            if(sum==s){
                return vector<int>({i+1,j+1});
            }
            else if(sum>s){
                sum -= arr[i];
                sum -= arr[j];
                i++;
            }
            else{
                j++;
            }

        }
        return vector<int>({-1,-1});
    }

};


